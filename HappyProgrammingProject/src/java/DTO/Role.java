package DTO;

/**
 *
 * @author okanh
 */
public class Role {
    private int ID;
    private String role;

    public Role(int ID, String role) {
        this.ID = ID;
        this.role = role;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    
}
